package Gun08;

public class StringToLoweCase {
    public static void main(String[] args) {

        //  ToLowerCase : stringi kucuk harfe çevirir.

        String text = "Merhaba Dünya";
        System.out.println("Original hali : " + text);
        System.out.println("kucuk harf hali : " + text.toLowerCase());






    }
}
