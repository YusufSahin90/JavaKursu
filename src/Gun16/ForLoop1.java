package Gun16;

public class ForLoop1 {

    public static void main(String[] args) {

        // While Loop

        int i = 0;
        while(i < 5){

            System.out.println("while ile i : " + i);
            i++;
        }
        for (i = 0; i < 5; i++){

            System.out.println("for ile i : " + i);
        }



    }
}
