public class WhileLoop3 {
    public static void main(String[] args) {

        // ekrana 5 kez merhaba yazdırınız.

        int sayac = 0;

        while(sayac < 5){
            System.out.println(sayac + ". kez Merhaba");
            sayac ++;  // sayac = sayac +1; oder saya+ = 1
        }



    }
}
