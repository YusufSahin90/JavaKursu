package Gun09;

public class JavaModul {
    public static void main(String[] args) {

        //  Modul : 2 sayının bölumundan kalanı verir.

        System.out.println("10 % 3 : " + (10 % 3));  // 10 un 3 e bolumunden kalan 1 dir.
        System.out.println("3 % 10 : " + (3 % 10));  //  modul işleminde sol taraf, sagdan kucuk ise
                                                     //  sonuç sol taraftaki sayıdır.

        System.out.println("11 % 2 : " + (11 % 2));  // 2 ye göre modulde sonu 1 veya 0 dır.
        System.out.println("10 % 2 : " + (10 % 2));  // bu bize sayının tek mi çift mi oldunu verir.








    }
}
